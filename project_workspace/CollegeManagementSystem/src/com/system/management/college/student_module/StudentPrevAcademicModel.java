package com.system.management.college.student_module;

public class StudentPrevAcademicModel {
	
	private String studentId;
	private String tenInstit;
	private String twelveInstit;
	private int tenMarks;
	private String twelveMarks;
	private String prevProjects;
	
	
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	public String getTenInstit() {
		return tenInstit;
	}
	public void setTenInstit(String tenInstit) {
		this.tenInstit = tenInstit;
	}
	public String getTwelveInstit() {
		return twelveInstit;
	}
	public void setTwelveInstit(String twelveInstit) {
		this.twelveInstit = twelveInstit;
	}
	public int getTenMarks() {
		return tenMarks;
	}
	public void setTenMarks(int tenMarks) {
		this.tenMarks = tenMarks;
	}
	public String getTwelveMarks() {
		return twelveMarks;
	}
	public void setTwelveMarks(String twelveMarks) {
		this.twelveMarks = twelveMarks;
	}
	public String getPrevProjects() {
		return prevProjects;
	}
	public void setPrevProjects(String prevProjects) {
		this.prevProjects = prevProjects;
	}
	
	

}
