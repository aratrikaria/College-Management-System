package com.system.management.college.faculty_module;

public class FacultyProfModel {

	private String facultyId;
	private String departmentId;
	private String gradInstit;
	private int gradMarks;
	private String postGradInstit;
	private int postGradMarks;
	private String currentDesign;
	
	
	public String getFacultyId() {
		return facultyId;
	}
	public void setFacultyId(String facultyId) {
		this.facultyId = facultyId;
	}
	public String getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(String departmentId) {
		this.departmentId = departmentId;
	}
	public String getGradInstit() {
		return gradInstit;
	}
	public void setGradInstit(String gradInstit) {
		this.gradInstit = gradInstit;
	}
	public int getGradMarks() {
		return gradMarks;
	}
	public void setGradMarks(int gradMarks) {
		this.gradMarks = gradMarks;
	}
	public String getPostGradInstit() {
		return postGradInstit;
	}
	public void setPostGradInstit(String postGradInstit) {
		this.postGradInstit = postGradInstit;
	}
	public int getPostGradMarks() {
		return postGradMarks;
	}
	public void setPostGradMarks(int postGradMarks) {
		this.postGradMarks = postGradMarks;
	}
	public String getCurrentDesign() {
		return currentDesign;
	}
	public void setCurrentDesign(String currentDesign) {
		this.currentDesign = currentDesign;
	}
	
	
	
}
